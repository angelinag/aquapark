    public void addForOneUpdateForTwo(int one) {
        String fname = txtfname.getText();
        String uname = txtuname.getText();
        String pass = txtPassword.getText();

        String day = comboDay.getSelectedItem().toString();
        int mon = comboMon.getSelectedIndex() + 1;
        String m = mon + "";
        if (mon < 10) {
            m = "0" + mon;
        }

        String year = comboYear.getSelectedItem().toString();
        String dob = year + "-" + m + "-" + day;

        String contact = txtContact.getText();
        String userType = comboUserType1.getSelectedItem().toString();

        int createdBy = UserDAOImp.userId;

        Date d = new Date();
        SimpleDateFormat df = new SimpleDateFormat();
        df.applyPattern("yyyy-MM-dd");

        String currentDate = df.format(d);

        UserBean userBean = new UserBean();
        userBean.setName(fname);
        userBean.setUserName(uname);
        userBean.setPassword(pass);
        userBean.setDOB(dob);
        userBean.setContact(contact);
        userBean.setUserType(userType);
        
        UserDAO userDAO = new UserDAOImp();
        if (one == 1) {
            if (userDAO.checkUser(userBean)) {
                JOptionPane.showMessageDialog(this, "User name already exits!");
            } else {
                
                userBean.setCreatedBy(createdBy);
                userBean.setCreatedDate(currentDate);
                
                if (userDAO.addUserDetails(userBean) != 0) {
                    try {
                        clearAll();
                        setTable();
                        JOptionPane.showMessageDialog(this, "User added successfully!");
                    } catch (SQLException ex) {
                        Logger.getLogger(UsersFrame.class.getName()).log(Level.SEVERE, null, ex);
                    }
                } else {
                    JOptionPane.showMessageDialog(this, "Failed to add the user!");
                }
            }
        } else if(one==2) {
            int id = (int) tableUserFrame.getValueAt(tableUserFrame.getSelectedRow(), 0);
            
            userBean.setModifiedBy(createdBy);
            userBean.setModifiedDate(currentDate);
            userBean.setUseriD(id);
            
            if (userDAO.updateUserDetails(userBean) != 0) {
                try {
                    clearAll();
                    setTable();
                    setButtons(2);
                    JOptionPane.showMessageDialog(this, "User updated successfully!");
                } catch (SQLException ex) {
                    Logger.getLogger(UsersFrame.class.getName()).log(Level.SEVERE, null, ex);
                }
            } else {
                JOptionPane.showMessageDialog(this, "Failed to update the user!");
            }
        }else if(one==3){
                int id = (int) tableUserFrame.getValueAt(tableUserFrame.getSelectedRow(), 0);
                userBean.setUseriD(id);
                if (userDAO.deleteUserDetails(userBean) != 0) {
                try {
                    clearAll();
                    setTable();
                    setButtons(2);
                    JOptionPane.showMessageDialog(this, "User deleted successfully!");
                } catch (SQLException ex) {
                    Logger.getLogger(UsersFrame.class.getName()).log(Level.SEVERE, null, ex);
                }
            } else {
                JOptionPane.showMessageDialog(this, "Failed to delete the user!");
            }
        }
    }
