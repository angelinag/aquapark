    private void setButtons(int val){
        if(val==1){
            btnAdd.setEnabled(false);
            btnUpdate.setEnabled(true);
            btnDelete.setEnabled(true);
        }else{
            btnAdd.setEnabled(true);
            btnUpdate.setEnabled(false);
            btnDelete.setEnabled(false);
        }
    }
    
    public void setAllTxts() {
        matchPasswords();
        setButtons(1);
        int id = (int) tableUserFrame.getValueAt(tableUserFrame.getSelectedRow(), 0);
        UserBean userBean = new UserBean();
        userBean.setUseriD(id);
        UserDAO userDAO = new UserDAOImp();
        ArrayList<UserBean> list = (ArrayList<UserBean>) userDAO.setAllTxt(userBean);
        for (UserBean userBean1 : list) {
            txtfname.setText(userBean1.getName());
            txtuname.setText(userBean1.getUserName());
            txtPassword1.setText(userBean1.getPassword());
            txtPassword.setText(userBean1.getPassword());
            String date[] = userBean1.getDOB().split("-");
            comboDay.setSelectedItem(date[2]);
            comboMon.setSelectedIndex(Integer.parseInt(date[1]) - 1);
            comboYear.setSelectedItem(date[0]);
            txtContact.setText(userBean1.getContact());
            comboUserType1.setSelectedItem(userBean1.getUserType());
        }
    }

